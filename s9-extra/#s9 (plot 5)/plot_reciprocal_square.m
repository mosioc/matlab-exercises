function plot_reciprocal_square()
    % تعریف بازه‌ای برای مقادیر x (به جز صفر)
    x = -10:0.1:-0.1; % بازه منفی برای x
    x = [x, 0.1:0.1:10]; % بازه مثبت برای x، به طوری که 0 در دامنه نباشد

    % محاسبه y = 1 / x^2
    y = 1 ./ (x.^2);

    % رسم نمودار y = 1 / x^2
    plot(x, y);

    % تنظیمات نمودار
    title('نمودار y = 1 / x^2');
    xlabel('x');
    ylabel('y');
    grid on; % نمایش شبکه‌بندی
end
