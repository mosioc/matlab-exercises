function multiplication_table()
    % دریافت عدد از کاربر
    n = input('لطفاً عددی را وارد کنید تا جدول ضرب آن ایجاد شود: ');

    % بررسی اینکه ورودی باید عدد صحیح مثبت باشد
    if n <= 0 || floor(n) ~= n
        disp('لطفاً یک عدد صحیح مثبت وارد کنید.');
        return;
    end

    % نمایش جدول ضرب
    disp(['جدول ضرب تا ' num2str(n) ':']);
    for i = 1:n  % حلقه برای عدد مورد نظر
        for j = 1:10  % حلقه برای ضرب تا 10
            product = i * j;  % محاسبه حاصل‌ضرب
            fprintf('%d x %d = %d\n', i, j, product);  % نمایش نتیجه
        end
        fprintf('\n');  % یک خط خالی برای جدا کردن جدول ضرب هر عدد
    end
end
