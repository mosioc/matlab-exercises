function plot_polynomial()
    % دریافت ضرایب چندجمله‌ای از کاربر
    coeffs = input('لطفاً ضرایب چندجمله‌ای را به صورت یک بردار سطری وارد کنید (مثال: [1, -3, 2]): ');

    % دریافت دامنه از کاربر
    domain = input('لطفاً دامنه را به صورت [start, end] وارد کنید (مثال: [0, 10]): ');

    % ایجاد مقادیر X بر اساس دامنه ورودی
    X = linspace(domain(1), domain(2), 100); % 100 نقطه در دامنه

    % محاسبه مقادیر Y با استفاده از چندجمله‌ای
    Y = polyval(coeffs, X);

    % رسم نمودار
    plot(X, Y);
    title('نمودار چندجمله‌ای');
    xlabel('x');
    ylabel('y');
    grid on; % نمایش شبکه‌بندی
end
